package homeworkweek6;

public class Program15 {
    public static void main(String[] args) {
        int a, b, temp;
        a = 20;
        b = 25;
        System.out.println("before swapping : a, b = "+a+", "+ + b);
        temp = a;
        a = b;
        b = temp;
        System.out.println("After swapping : a, b = "+a+", "+ + b);
    }

}
